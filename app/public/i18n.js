const RTL_LANGS = ["ar", "fa"];

const TRANSLATIONS = {};

function registerTranslations(lang, dict) {
  TRANSLATIONS[lang] = { ...(TRANSLATIONS[lang] || {}), ...dict };
}

function detectLanguage() {
  const saved = localStorage.getItem("lang");
  if (saved && LANGUAGES.some((l) => l.code === saved)) return saved;
  const browserLangs = navigator.languages || [navigator.language];
  for (const bl of browserLangs) {
    const short = bl.slice(0, 2).toLowerCase();
    if (LANGUAGES.some((l) => l.code === short)) return short;
  }
  return "sv";
}

let currentLang = "sv";

function t(key) {
  return (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) || (TRANSLATIONS.sv && TRANSLATIONS.sv[key]) || key;
}

function applyTranslations() {
  document.documentElement.lang = currentLang;
  document.documentElement.dir = RTL_LANGS.includes(currentLang) ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"));
  });
  const flagEl = document.querySelector(".se-flag");
  if (flagEl) {
    const lang = LANGUAGES.find((l) => l.code === currentLang);
    flagEl.textContent = lang ? lang.flag : "🇸🇪";
  }
  const themeBtn = document.getElementById("theme-toggle");
  if (themeBtn) {
    const theme = document.documentElement.getAttribute("data-theme") || "dark";
    themeBtn.textContent = t(`theme_${theme}`);
  }
}

function setLanguage(lang) {
  currentLang = LANGUAGES.some((l) => l.code === lang) ? lang : "sv";
  localStorage.setItem("lang", currentLang);
  applyTranslations();
}

function populateLanguageSelect() {
  const select = document.getElementById("lang-select");
  if (!select) return;
  select.innerHTML = "";
  for (const l of LANGUAGES) {
    const opt = document.createElement("option");
    opt.value = l.code;
    opt.textContent = `${l.flag} ${l.native}`;
    select.appendChild(opt);
  }
  select.value = currentLang;
  select.addEventListener("change", (e) => setLanguage(e.target.value));
}

function initI18n() {
  currentLang = detectLanguage();
  populateLanguageSelect();
  applyTranslations();
}

